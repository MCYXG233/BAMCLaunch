class AesEncryption {
  static const int _keyLength = 32;
  static const int _ivLength = 16;

  static Uint8List _generateKey(String seed) {
    final key = sha256.convert(utf8.encode(seed)).bytes;
    return Uint8List.fromList(key);
  }

  static Uint8List _generateIV() {
    final random = Random.secure();
    return Uint8List.fromList(List.generate(_ivLength, (_) => random.nextInt(256)));
  }

  static String encrypt(String plaintext, String key) {
    final encryptionKey = _generateKey(key);
    final iv = _generateIV();
    
    final cipher = AES(encryptionKey);
    final encrypted = cipher.encryptCbc(utf8.encode(plaintext), iv);
    
    final encryptedData = Uint8List(iv.length + encrypted.length);
    encryptedData.setRange(0, iv.length, iv);
    encryptedData.setRange(iv.length, encryptedData.length, encrypted);
    
    return base64.encode(encryptedData);
  }

  static String decrypt(String ciphertext, String key) {
    final encryptedData = base64.decode(ciphertext);
    
    final iv = encryptedData.sublist(0, _ivLength);
    final encrypted = encryptedData.sublist(_ivLength);
    
    final encryptionKey = _generateKey(key);
    final cipher = AES(encryptionKey);
    
    final decrypted = cipher.decryptCbc(encrypted, iv);
    
    return utf8.decode(decrypted);
  }
}

class AES {
  final Uint8List key;

  AES(this.key);

  Uint8List encryptCbc(List<int> plaintext, Uint8List iv) {
    final padded = _pad(plaintext);
    final ciphertext = Uint8List(padded.length);
    
    for (var i = 0; i< padded.length; i += 16) {
      final block = padded.sublist(i, i + 16);
      final xorBlock = _xor(block, iv);
      final encryptedBlock = _encryptBlock(xorBlock);
      ciphertext.setRange(i, i + 16, encryptedBlock);
      iv = encryptedBlock;
    }
    
    return ciphertext;
  }

  Uint8List decryptCbc(Uint8List ciphertext, Uint8List iv) {
    final plaintext = Uint8List(ciphertext.length);
    
    for (var i = 0; i < ciphertext.length; i += 16) {
      final block = ciphertext.sublist(i, i + 16);
      final decryptedBlock = _decryptBlock(block);
      final xorBlock = _xor(decryptedBlock, iv);
      plaintext.setRange(i, i + 16, xorBlock);
      iv = block;
    }
    
    return _unpad(plaintext);
  }

  Uint8List _pad(List<int> data) {
    final paddingLength = 16 - (data.length % 16);
    final padded = Uint8List(data.length + paddingLength);
    padded.setRange(0, data.length, data);
    for (var i = data.length; i < padded.length; i++) {
      padded[i] = paddingLength;
    }
    return padded;
  }

  Uint8List _unpad(Uint8List data) {
    final paddingLength = data[data.length - 1];
    return data.sublist(0, data.length - paddingLength);
  }

  Uint8List _xor(List<int> a, Uint8List b) {
    final result = Uint8List(a.length);
    for (var i = 0; i < a.length; i++) {
      result[i] = a[i] ^ b[i];
    }
    return result;
  }

  Uint8List _encryptBlock(List<int> block) {
    return _subBytes(_shiftRows(_mixColumns(_addRoundKey(Uint8List.fromList(block), key))));
  }

  Uint8List _decryptBlock(Uint8List block) {
    return _addRoundKey(_mixColumnsInv(_shiftRowsInv(_subBytesInv(block))), key);
  }

  Uint8List _addRoundKey(Uint8List block, Uint8List key) {
    final result = Uint8List(block.length);
    for (var i = 0; i < block.length; i++) {
      result[i] = block[i] ^ key[i];
    }
    return result;
  }

  Uint8List _subBytes(List<int> block) {
    final result = Uint8List(block.length);
    for (var i = 0; i < block.length; i++) {
      result[i] = _sBox[block[i]];
    }
    return result;
  }

  Uint8List _subBytesInv(Uint8List block) {
    final result = Uint8List(block.length);
    for (var i = 0; i < block.length; i++) {
      result[i] = _invSBox[block[i]];
    }
    return result;
  }

