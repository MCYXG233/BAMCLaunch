    result[3] = _gfMul(11, col[0]) ^ _gfMul(13, col[1]) ^ _gfMul(9, col[2]) ^ _gfMul(14, col[3]);
    return result;
  }

  int _gfMul(int a, int b) {
    var p = 0;
    for (var i = 0; i < 8; i++) {
      if ((b & 1) != 0) {
        p ^= a;
      }
      final hiBitSet = (a & 0x80) != 0;
      a <<= 1;
      if (hiBitSet) {
        a ^= 0x1b;
      }
      b >>= 1;
    }
    return p;
  }

