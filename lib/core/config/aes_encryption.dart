  Uint8List _mixColumns(Uint8List block) {
    final result = Uint8List(16);
    for (var i = 0; i < 4; i++) {
      final col = block.sublist(i * 4, i * 4 + 4);
      final mixed = _mixColumn(col);
      result.setRange(i * 4, i * 4 + 4, mixed);
    }
    return result;
  }

  Uint8List _mixColumnsInv(Uint8List block) {
    final result = Uint8List(16);
    for (var i = 0; i < 4; i++) {
      final col = block.sublist(i * 4, i * 4 + 4);
      final mixed = _mixColumnInv(col);
      result.setRange(i * 4, i * 4 + 4, mixed);
    }
    return result;
  }

  Uint8List _mixColumn(List<int> col) {
    final result = Uint8List(4);
    result[0] = _gfMul(2, col[0]) ^ _gfMul(3, col[1]) ^ col[2] ^ col[3];
    result[1] = col[0] ^ _gfMul(2, col[1]) ^ _gfMul(3, col[2]) ^ col[3];
    result[2] = col[0] ^ col[1] ^ _gfMul(2, col[2]) ^ _gfMul(3, col[3]);
    result[3] = _gfMul(3, col[0]) ^ col[1] ^ col[2] ^ _gfMul(2, col[3]);
    return result;
  }

  Uint8List _mixColumnInv(List<int> col) {
    final result = Uint8List(4);
    result[0] = _gfMul(14, col[0]) ^ _gfMul(11, col[1]) ^ _gfMul(13, col[2]) ^ _gfMul(9, col[3]);
