import 'dart:io';

/// 平台适配器统一接口
///
/// 该接口定义了跨平台文件系统操作和路径获取的抽象方法，
/// 用于屏蔽不同操作系统之间的差异，提供统一的平台相关功能访问接口。
///
/// 实现类需要根据具体平台（Windows、macOS、Linux）提供相应的实现逻辑。
/// 通过依赖注入的方式，可以在运行时切换不同的平台适配器实现，
/// 从而实现跨平台兼容性。
///
/// 典型使用场景：
/// - 获取应用专属存储目录
/// - 查找系统Java安装路径
/// - 管理游戏数据目录
/// - 处理平台特定的文件路径和权限问题
abstract class IPlatformAdapter {
  /// 获取应用支持目录
  ///
  /// 返回应用专属的存储目录路径，用于存放应用配置、缓存等数据。
  /// 不同平台返回的路径如下：
  /// - Windows: `%APPDATA%\<应用名>`
  /// - macOS: `~/Library/Application Support/<应用名>`
  /// - Linux: `~/.local/share/<应用名>`
  ///
  /// 该目录通常用于存储：
  /// - 应用配置文件
  /// - 用户数据
  /// - 日志文件
  /// - 缓存数据
  ///
  /// 返回应用专属存储目录的绝对路径。
  /// 如果目录不存在，实现类应当自动创建。
  ///
  /// 可能抛出 [FileSystemException] 当无法访问或创建目录时。
  Future<String> getApplicationSupportDirectory();

  /// 获取默认游戏目录
  ///
  /// 返回游戏数据的默认存储路径。
  /// 该目录用于存放游戏资源、存档、模组等数据。
  ///
  /// 不同平台返回的路径可能有所不同：
  /// - Windows: 通常在用户文档目录或应用数据目录下
  /// - macOS: 通常在用户 Application Support 目录下
  /// - Linux: 通常在用户 .local/share 目录下
  ///
  /// 返回游戏数据默认存储目录的绝对路径。
  /// 如果目录不存在，实现类应当自动创建。
  ///
  /// 可能抛出 [FileSystemException] 当无法访问或创建目录时。
  Future<String> getDefaultGameDirectory();

  /// 获取默认Java路径
  ///
  /// 返回系统默认Java可执行文件路径。
  /// 该方法会尝试在系统中查找已安装的Java运行环境，
  /// 并返回首选的Java可执行文件路径。
  ///
  /// 查找顺序通常为：
  /// 1. JAVA_HOME 环境变量指定的路径
  /// 2. 系统默认安装路径
  /// 3. PATH 环境变量中的 java 命令
  ///
  /// 返回Java可执行文件的绝对路径。
  /// 如果系统中未找到Java安装，可能返回空字符串或抛出异常。
  ///
  /// 注意：调用方应当检查返回值的有效性。
  Future<String> getDefaultJavaPath();

  /// 获取临时目录
  ///
  /// 返回系统临时文件目录路径，用于存放临时文件。
  /// 该目录中的文件可能会被系统定期清理。
  ///
  /// 不同平台返回的路径如下：
  /// - Windows: `%TEMP%` 或 `%TMP%` 环境变量指定的路径
  /// - macOS: `/tmp` 或 `~/Library/Caches`
  /// - Linux: `/tmp` 或 `~/.cache`
  ///
  /// 返回系统临时目录的绝对路径。
  /// 该目录应当始终存在且可写。
  ///
  /// 可能抛出 [FileSystemException] 当无法访问临时目录时。
  Future<String> getTempDirectory();

  /// 确保目录存在
  ///
  /// 检查指定目录是否存在，如果不存在则递归创建该目录及其所有父目录。
  /// 该方法类似于 `mkdir -p` 命令。
  ///
  /// 参数：
  /// - [path] 要确保存在的目录路径，可以是相对路径或绝对路径。
  ///
  /// 如果目录已存在，该方法不执行任何操作。
  /// 如果目录不存在，将创建该目录及其所有必要的父目录。
  ///
  /// 可能抛出以下异常：
  /// - [FileSystemException] 当无法创建目录时（如权限不足、路径无效等）
  /// - [ArgumentError] 当 [path] 为空字符串或 null 时
  Future<void> ensureDirectoryExists(String path);

  /// 检查写权限
  ///
  /// 检查当前用户对指定路径是否具有写权限。
  /// 该方法用于在执行写入操作前验证权限，避免运行时错误。
  ///
  /// 参数：
  /// - [path] 要检查的路径，可以是文件或目录路径。
  ///
  /// 返回 `true` 表示当前用户对该路径有写权限，
  /// 返回 `false` 表示没有写权限或路径不存在。
  ///
  /// 注意：
  /// - 如果路径不存在，返回 `false`
  /// - 对于文件，检查文件本身的写权限
  /// - 对于目录，检查目录的写权限（即能否在其中创建文件）
  Future<bool> hasWritePermission(String path);

  /// 查找系统中的Java安装
  ///
  /// 扫描系统中所有可能的位置，查找已安装的Java运行环境，
  /// 并返回找到的所有Java可执行文件路径列表。
  ///
  /// 查找位置包括：
  /// - JAVA_HOME 环境变量
  /// - 系统默认安装路径（如 Windows 的 Program Files）
  /// - 用户目录下的安装
  /// - PATH 环境变量中的 java 命令
  ///
  /// 返回找到的所有Java可执行文件路径列表。
  /// 如果未找到任何Java安装，返回空列表。
  ///
  /// 返回列表中的路径顺序通常按优先级排序，
  /// 第一个元素通常是系统推荐的Java版本。
  ///
  /// 注意：该方法可能需要较长时间执行，建议在后台线程调用。
  Future<List<String>> findJavaInstallations();

  /// 是否Windows平台
  ///
  /// 返回当前运行环境是否为 Windows 操作系统。
  /// 用于在代码中进行平台特定的逻辑分支。
  ///
  /// 返回 `true` 表示当前平台为 Windows，否则返回 `false`。
  bool get isWindows;

  /// 是否MacOS平台
  ///
  /// 返回当前运行环境是否为 macOS 操作系统。
  /// 用于在代码中进行平台特定的逻辑分支。
  ///
  /// 返回 `true` 表示当前平台为 macOS，否则返回 `false`。
  bool get isMacOS;

  /// 是否Linux平台
  ///
  /// 返回当前运行环境是否为 Linux 操作系统。
  /// 用于在代码中进行平台特定的逻辑分支。
  ///
  /// 返回 `true` 表示当前平台为 Linux，否则返回 `false`。
  bool get isLinux;
}